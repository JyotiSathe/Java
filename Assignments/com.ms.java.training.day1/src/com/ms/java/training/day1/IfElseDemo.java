package com.ms.java.training.day1;

public class IfElseDemo {

	public static void main(String args[]) {

		// The if-then Statement (means if true then execute the statement)
		int i = 5;

		if (i == 5) {
			System.out.println("Print if condition is true 5");
		}

		if (i ==4) {
			System.out.println("Print if condition is true 4");
		}
		
		
		// The if-then-else Statement
		
		if (i == 5||i==6) {
			System.out.println("Print if condition is true in  if else");
		}else 
		{
			System.out.println("Print if condition is false if else");
		}
		

	}

}
